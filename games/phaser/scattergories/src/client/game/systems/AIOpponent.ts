const AI_NAMES_BY_DIFFICULTY: Record<AIDifficulty, string[]> = {
  easy: ['Rookie Bot'],
  medium: ['Bot Alpha', 'Bot Beta'],
  hard: ['CPU Omega', 'CPU Sigma', 'CPU Delta'],
};

const DIFFICULTY_CONFIG: Record<AIDifficulty, { count: number; skillMin: number; skillMax: number }> = {
  easy: { count: 1, skillMin: 0.25, skillMax: 0.45 },
  medium: { count: 2, skillMin: 0.45, skillMax: 0.65 },
  hard: { count: 3, skillMin: 0.70, skillMax: 0.90 },
};

export function createAIPlayers(count?: number, difficulty: AIDifficulty = 'medium'): AIPlayer[] {
  const config = DIFFICULTY_CONFIG[difficulty];
  const n = count ?? config.count;
  const names = AI_NAMES_BY_DIFFICULTY[difficulty];
  return Array.from({ length: n }, (_, i) => ({
    id: `ai-${i + 1}`,
    name: names[i] ?? `Bot ${i + 1}`,
    skill: config.skillMin + Math.random() * (config.skillMax - config.skillMin),
  }));
}

export function generateAIAnswers(
  ai: AIPlayer,
  categories: string[],
  letter: string,
): string[] {
  return categories.map((cat) => {
    if (Math.random() > ai.skill) return '';

    const bankKey = categorize(cat);
    const bank = WORD_BANK[bankKey] ?? WORD_BANK['thing']!;
    const matches = getMatchingWords(bank, letter);

    if (matches.length === 0) return '';

    return matches[Math.floor(Math.random() * matches.length)]!;
  });
}
