function categorize(category: string): string {
  const lower = category.toLowerCase();
  if (lower.includes('name') || lower.includes('people') || lower.includes('famous') || lower.includes('athlete') || lower.includes('leader') || lower.includes('comedian') || lower.includes('scientist')) return 'name';
  if (lower.includes('city') || lower.includes('cities')) return 'city';
  if (lower.includes('vacation') || lower.includes('spot') || lower.includes('restaurant')) return 'city';
  if (lower.includes('countr') || lower.includes('language')) return 'country';
  if (lower.includes('animal') || lower.includes('insect') || lower.includes('dog breed') || lower.includes('zoo')) return 'animal';
  if (lower.includes('bird')) return 'animal';
  if (lower.includes('fish') || lower.includes('reptile') || lower.includes('seafood')) return 'animal';
  if (lower.includes('flower')) return 'flower';
  if (lower.includes('tree')) return 'tree';
  if (lower.includes('color')) return 'color';
  if (lower.includes('instrument')) return 'instrument';
  if (lower.includes('car') && lower.includes('brand')) return 'car';
  if (lower.includes('holiday')) return 'holiday';
  if (lower.includes('toy') || lower.includes('board game')) return 'toy';
  if (lower.includes('superhero') || lower.includes('villain') || lower.includes('cartoon') || lower.includes('fairy tale')) return 'superhero';
  if (lower.includes('mytholog') || lower.includes('creature')) return 'mythological';
  if (lower.includes('emotion')) return 'emotion';
  if (lower.includes('brand') || lower.includes('store') || lower.includes('app') || lower.includes('website')) return 'brand';
  if (lower.includes('bodies of water') || lower.includes('water')) return 'body_of_water';
  if (lower.includes('food') || lower.includes('fruit') || lower.includes('vegetable') || lower.includes('breakfast') || lower.includes('dessert') || lower.includes('snack') || lower.includes('candy') || lower.includes('pizza') || lower.includes('pasta') || lower.includes('cereal') || lower.includes('baked') || lower.includes('ice cream') || lower.includes('sandwich') || lower.includes('condiment') || lower.includes('beverage')) return 'food';
  if (lower.includes('sport') || lower.includes('hobb') || lower.includes('dance') || lower.includes('game')) return 'sport';
  if (lower.includes('cloth') || lower.includes('wear') || lower.includes('head')) return 'clothing';
  if (lower.includes('tv') || lower.includes('show') || lower.includes('movie') || lower.includes('song') || lower.includes('character') || lower.includes('book') || lower.includes('magazine') || lower.includes('musical group')) return 'show';
  if (lower.includes('occupation') || lower.includes('job') || lower.includes('major')) return 'occupation';
  return 'thing';
}

function getMatchingWords(bank: string[], letter: string): string[] {
  const l = letter.toUpperCase();
  return bank.filter((w) => w.toUpperCase().startsWith(l));
}

